    /**
     * Reconstructs the {@code rsqrtps} seed for a strictly positive normal input.
     *
     * <p>The seed table is keyed on the exponent parity and the top ten mantissa bits; the looked-up canonical
     * result is rescaled to the input exponent by subtracting {@code (e - parity) / 2} from its biased
     * exponent, mirroring {@code rsqrt(2^e * m) = 2^(-e/2) * rsqrt(m)}.
     *
     * @param a the strictly positive normal input
     * @return the {@code rsqrtps} approximation of {@code 1 / sqrt(a)}
     */
    private static float rsqrtps(float a) {
        int bits = Float.floatToRawIntBits(a);
        int biasedExp = (bits >>> 23) & 0xff;
        int index = (bits & 0x7fffff) >>> 13;
        int exp = biasedExp - 127;
        int parity = (exp & 1) != 0 ? 1 : 0;
        int seedMantissa = (RSQRT_SEED[parity * INDEX_SIZE + index] & 0xffff) << 11;
        int seedBits = 0x3f000000 | seedMantissa;
        int resultExp = ((seedBits >>> 23) & 0xff) - (exp - parity) / 2;
        return Float.intBitsToFloat((seedBits & 0x807fffff) | (resultExp << 23));
    }

    /**
     * Computes the {@code -Ofast} approximate single-precision square root of a non-negative value, the per-lane
     * body of {@code smpl_sqrt_vec}.
     *
     * <p>A zero input returns zero; otherwise the {@code rsqrtps} seed is refined by one Newton-Raphson step in
     * the exact instruction order the reference schedule emits.
     *
     * @param a the non-negative value to take the square root of
     * @return the approximate square root matching the {@code -Ofast} {@code smpl_sqrt_vec} bit pattern
     */
    public static float sqrt(float a) {
        if (a == 0.0f) {
            return 0.0f;
        }
        float x0 = rsqrtps(a);
        float ax0 = a * x0;
        float refined = ax0 * x0;
        float halfAx0 = ax0 * -0.5f;
        return halfAx0 * (refined - 3.0f);
    }

