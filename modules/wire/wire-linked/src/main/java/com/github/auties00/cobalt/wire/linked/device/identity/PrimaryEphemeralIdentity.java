package com.github.auties00.cobalt.wire.linked.device.identity;

import it.auties.protobuf.annotation.*;
import it.auties.protobuf.model.*;
import java.util.Optional;

/**
 * Ephemeral identity that the primary device contributes to the pairing handshake.
 *
 * <p>Once the companion has opened its {@link CompanionCommitment} by revealing its
 * {@link CompanionEphemeralIdentity}, the primary replies with this structure. It
 * contains the primary's ephemeral Curve25519 public key plus a fresh random nonce; both
 * values feed the Noise handshake that derives the symmetric keys used to protect the
 * remainder of the pairing exchange.
 *
 * <p>Unlike the companion's identity, the primary version does not advertise a platform
 * type or a reference string: the companion already knows which primary it is talking to
 * (the user has just scanned a QR code generated by that specific account), so only the
 * keying material has to be transmitted.
 */
@ProtobufMessage(name = "PrimaryEphemeralIdentity")
public final class PrimaryEphemeralIdentity {
    /**
     * Ephemeral Curve25519 public key chosen by the primary for this pairing session.
     *
     * <p>Combines with the companion's ephemeral key to produce the shared secret that
     * keys the pairing channel. Discarded once pairing completes so that compromise of
     * the primary later on does not affect past pairing sessions.
     */
    @ProtobufProperty(index = 1, type = ProtobufType.BYTES)
    byte[] publicKey;

    /**
     * Random nonce mixed into the pairing handshake to guarantee freshness.
     *
     * <p>Sampled anew for every pairing attempt. It ensures that even if the primary
     * accidentally reuses the same ephemeral key across two pairings, the derived session
     * keys remain distinct, and any replay of captured pairing traffic fails with a
     * detectable mismatch.
     */
    @ProtobufProperty(index = 2, type = ProtobufType.BYTES)
    byte[] nonce;

    /**
     * Constructs a primary ephemeral identity with the given key material. Package-private:
     * use the generated {@code PrimaryEphemeralIdentityBuilder} or the protobuf decoder.
     *
     * @param publicKey the ephemeral Curve25519 public key, or {@code null}
     * @param nonce     the random pairing nonce, or {@code null}
     */
    PrimaryEphemeralIdentity(byte[] publicKey, byte[] nonce) {
        this.publicKey = publicKey;
        this.nonce = nonce;
    }

    /**
     * Returns the ephemeral public key chosen by the primary.
     *
     * @return the key bytes, or {@link Optional#empty()} when the field was absent
     */
    public Optional<byte[]> publicKey() {
        return Optional.ofNullable(publicKey);
    }

    /**
     * Returns the random pairing nonce.
     *
     * @return the nonce bytes, or {@link Optional#empty()} when the field was absent
     */
    public Optional<byte[]> nonce() {
        return Optional.ofNullable(nonce);
    }

    /**
     * Sets the ephemeral public key.
     *
     * @param publicKey the new key bytes, or {@code null} to clear
     */
    public void setPublicKey(byte[] publicKey) {
        this.publicKey = publicKey;
    }

    /**
     * Sets the pairing nonce.
     *
     * @param nonce the new nonce bytes, or {@code null} to clear
     */
    public void setNonce(byte[] nonce) {
        this.nonce = nonce;
    }
}
