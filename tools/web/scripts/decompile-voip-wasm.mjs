function normalizePath(s) {
  const i = s.search(/xplat\//);
  if (i < 0) return null;
  return s.slice(i).replace(/\\/g, "/").split(/[\s"'():;,<>]+/)[0];
}

function mostCommon(values) {
  const counts = new Map();
  for (const v of values) counts.set(v, (counts.get(v) || 0) + 1);
  return [...counts.entries()].sort((a, b) => b[1] - a[1] || String(a[0]).localeCompare(String(b[0])))[0]?.[0] || null;
}

function primarySourcePath(refs, fn) {
  const strings = refs.byFunction.get(fn) || [];
  const hits = [];
  for (const s of strings) {
    const direct = normalizePath(s);
    if (direct) hits.push(direct);
    for (const tok of s.split(/[\s"'(),;<>]+/)) {
      const p = normalizePath(tok);
      if (p) hits.push(p);
    }
  }
  const code = hits.filter((p) => /\.(cc|cpp|c)$/.test(p));
  if (code.length) return mostCommon(code);
  const headers = hits.filter((p) => /\.(h|hpp)$/.test(p));
  if (headers.length) return mostCommon(headers);
  return hits.length ? mostCommon(hits) : null;
}

