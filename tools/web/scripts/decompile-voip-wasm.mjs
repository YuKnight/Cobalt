function parseArgs(argv) {
  const opts = { force: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    const next = () => {
      if (i + 1 >= argv.length) throw new Error(`missing value for ${a}`);
      return argv[++i];
    };
    if (a === "--wasm") opts.wasm = next();
    else if (a === "--out") opts.out = next();
    else if (a === "--module") opts.module = next();
    else if (a === "--ghidra") opts.ghidra = next();
    else if (a === "--java-home") opts.javaHome = next();
    else if (a === "--force") opts.force = true;
    else if (a === "--help" || a === "-h") opts.help = true;
    else throw new Error(`unknown argument: ${a}`);
  }
  if (opts.wasm) opts.wasm = path.resolve(ROOT, opts.wasm);
  if (opts.ghidra) opts.ghidra = path.resolve(ROOT, opts.ghidra);
  if (opts.javaHome) opts.javaHome = path.resolve(ROOT, opts.javaHome);
  return opts;
}

