  for (const f of analysis.functions) {
    const p = primarySourcePath(refs, f.index);
    if (p) sourcePathByFn.set(f.index, p);
    if (p && CALL_PROJECT_PATH_RE.test(p)) sourceRoots.push(f.index);
  }
  const importIndex = allFunctionImportIndices(analysis);
