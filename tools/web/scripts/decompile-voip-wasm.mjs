class BinaryReader {
  constructor(buffer) {
    this.bytes = buffer instanceof Buffer
      ? new Uint8Array(buffer.buffer, buffer.byteOffset, buffer.byteLength)
      : buffer;
    this.view = new DataView(this.bytes.buffer, this.bytes.byteOffset, this.bytes.byteLength);
    this.pos = 0;
  }
  get length() { return this.bytes.length; }
  readByte() { return this.bytes[this.pos++]; }
  readU32Fixed() {
    const value = this.view.getUint32(this.pos, true);
    this.pos += 4;
    return value;
  }
  readU32Leb() {
    let result = 0, shift = 0, byte;
    do {
      byte = this.bytes[this.pos++];
      result |= (byte & 0x7f) << shift;
      shift += 7;
    } while (byte & 0x80);
    return result >>> 0;
  }
  readI32Leb() {
    let result = 0, shift = 0, byte;
    do {
      byte = this.bytes[this.pos++];
      result |= (byte & 0x7f) << shift;
      shift += 7;
    } while (byte & 0x80);
    if (shift < 32 && byte & 0x40) result |= -(1 << shift);
    return result;
  }
  readI64Leb() {
    let result = 0n, shift = 0n, byte;
    do {
      byte = this.bytes[this.pos++];
      result |= BigInt(byte & 0x7f) << shift;
      shift += 7n;
    } while (byte & 0x80);
    if (shift < 64n && byte & 0x40) result |= -(1n << shift);
    return result;
  }
  readS33() {
    let result = 0n, shift = 0n, byte;
    do {
      byte = this.bytes[this.pos++];
      result |= BigInt(byte & 0x7f) << shift;
      shift += 7n;
    } while (byte & 0x80);
    if (byte & 0x40) result |= -(1n << shift);
    return Number(result);
  }
  readF32() {
    const value = this.view.getFloat32(this.pos, true);
    this.pos += 4;
    return value;
  }
  readF64() {
    const value = this.view.getFloat64(this.pos, true);
    this.pos += 8;
    return value;
  }
  readName() {
    const len = this.readU32Leb();
    const bytes = this.bytes.slice(this.pos, this.pos + len);
    this.pos += len;
    return new TextDecoder().decode(bytes);
  }
  readValType() {
    const byte = this.readByte();
    return VAL_TYPE_NAMES[byte] ?? `unknown(0x${byte.toString(16)})`;
  }
  readVec(reader) {
    const count = this.readU32Leb();
    const out = [];
    for (let i = 0; i < count; i++) out.push(reader());
    return out;
  }
  skip(n) { this.pos += n; }
}

const SHAPE = new Uint8Array(256).fill(0);
const setRange = (lo, hi, shape) => { for (let op = lo; op <= hi; op++) SHAPE[op] = shape; };
SHAPE[0x00] = 1; SHAPE[0x01] = 1; SHAPE[0x02] = 8; SHAPE[0x03] = 8; SHAPE[0x04] = 8; SHAPE[0x05] = 1; SHAPE[0x0b] = 1;
SHAPE[0x0c] = 2; SHAPE[0x0d] = 2; SHAPE[0x0e] = 11; SHAPE[0x0f] = 1; SHAPE[0x10] = 2; SHAPE[0x11] = 3; SHAPE[0x12] = 2; SHAPE[0x13] = 3;
SHAPE[0x1a] = 1; SHAPE[0x1b] = 1; SHAPE[0x1c] = 12; setRange(0x20, 0x24, 2); SHAPE[0x25] = 2; SHAPE[0x26] = 2;
setRange(0x28, 0x3e, 9); SHAPE[0x3f] = 2; SHAPE[0x40] = 2; SHAPE[0x41] = 4; SHAPE[0x42] = 5; SHAPE[0x43] = 6; SHAPE[0x44] = 7;
setRange(0x45, 0xc4, 1); SHAPE[0xd0] = 16; SHAPE[0xd1] = 1; SHAPE[0xd2] = 2;

function prefixShape(prefix, sub) {
  if (prefix === 0xfc) {
    if (sub <= 7) return 1;
    switch (sub) {
      case 8: case 10: case 12: case 14: return 3;
      case 9: case 11: case 13: case 15: case 16: case 17: return 2;
      default: return 0;
    }
  }
  if (prefix === 0xfd) {
    if (sub <= 0x0b) return 9;
    if (sub === 0x0c) return 13;
    if (sub === 0x0d) return 14;
    if (sub >= 0x15 && sub <= 0x22) return 15;
    if (sub >= 0x54 && sub <= 0x5b) return 10;
    if (sub >= 0x5c && sub <= 0x5d) return 9;
    return 1;
  }
  if (prefix === 0xfe) return sub === 0x03 ? 17 : 9;
  return 0;
}

function readMemarg(r) {
  const alignFlags = r.readU32Leb();
  if (alignFlags & 0x40) r.readU32Leb();
  const offset = r.readU32Leb();
  return { align: alignFlags & ~0x40, offset };
}

function consumeImmediates(r, shape) {
  switch (shape) {
    case 2: r.readU32Leb(); break;
    case 3: r.readU32Leb(); r.readU32Leb(); break;
    case 4: r.readI32Leb(); break;
    case 5: r.readI64Leb(); break;
    case 6: r.skip(4); break;
    case 7: r.skip(8); break;
    case 8: r.readS33(); break;
    case 9: readMemarg(r); break;
    case 10: readMemarg(r); r.skip(1); break;
    case 11: {
      const n = r.readU32Leb();
      for (let i = 0; i < n; i++) r.readU32Leb();
      r.readU32Leb();
      break;
    }
    case 12: r.skip(r.readU32Leb()); break;
    case 13:
    case 14: r.skip(16); break;
    case 15:
    case 16:
    case 17: r.skip(1); break;
  }
}

function decodeConstExpr(r) {
  let result = { kind: "unknown" };
  while (r.pos < r.length) {
    const op = r.readByte();
    if (op === 0x0b) break;
    switch (op) {
      case 0x41: result = { kind: "i32", value: r.readI32Leb() }; break;
      case 0x42: result = { kind: "i64", value: r.readI64Leb().toString() }; break;
      case 0x43: result = { kind: "f32", value: r.readF32() }; break;
      case 0x44: result = { kind: "f64", value: r.readF64() }; break;
      case 0x23: result = { kind: "global", index: r.readU32Leb() }; break;
      case 0xd2: result = { kind: "ref.func", index: r.readU32Leb() }; break;
      case 0xd0: r.skip(1); result = { kind: "ref.null" }; break;
      default: {
        const shape = op === 0xfc || op === 0xfd || op === 0xfe
          ? prefixShape(op, r.readU32Leb())
          : SHAPE[op];
        consumeImmediates(r, shape);
      }
    }
  }
  return result;
}

function walkFunctionBody(r, bodyOffset, bodySize, visitor) {
  const bodyEnd = bodyOffset + bodySize;
  r.pos = bodyOffset;
  const localGroups = r.readU32Leb();
  for (let i = 0; i < localGroups; i++) {
    r.readU32Leb();
    r.readByte();
  }
  let depth = 0;
  const constWindow = [];
  while (r.pos < bodyEnd) {
    const at = r.pos;
    const opcode = r.readByte();
    if (opcode === 0x0b) {
      if (depth === 0) {
        r.pos = bodyEnd;
        return { partial: false };
      }
      depth--;
      constWindow.length = 0;
      visitor.onBlockEnd?.(at);
      continue;
    }
    if (opcode === 0x05) {
      constWindow.length = 0;
      visitor.onElse?.(at);
      continue;
    }
    let sub = -1;
    const shape = opcode === 0xfc || opcode === 0xfd || opcode === 0xfe
      ? ((sub = r.readU32Leb()), prefixShape(opcode, sub))
      : SHAPE[opcode];
    if (shape === 0) {
      r.pos = bodyEnd;
      return { partial: true, stoppedAt: at, unknownOpcode: opcode };
    }
    let pushedConst = null;
    switch (shape) {
      case 1: break;
      case 2: {
        const v = r.readU32Leb();
        if (opcode === 0x10 || opcode === 0x12) visitor.onCall?.(v, at);
        else if (opcode === 0xd2) visitor.onRefFunc?.(v, at);
        break;
      }
      case 3: {
        const a = r.readU32Leb();
        const b = r.readU32Leb();
        if (opcode === 0x11 || opcode === 0x13) visitor.onCallIndirect?.(a, b, at);
        else if (opcode === 0xfc && sub === 8) {
          const dest = constWindow.length >= 3 ? constWindow[constWindow.length - 3] : null;
          visitor.onMemoryInit?.(a, dest, at);
        }
        break;
      }
      case 4: {
        const v = r.readI32Leb();
        if (opcode === 0x41) {
          pushedConst = v;
          visitor.onI32Const?.(v, at);
        }
        break;
      }
      case 5: r.readI64Leb(); break;
      case 6: r.skip(4); break;
      case 7: r.skip(8); break;
      case 8: r.readS33(); depth++; visitor.onBlockStart?.(opcode, at); break;
      case 9: {
        const { align, offset } = readMemarg(r);
        visitor.onMemAccess?.(opcode, offset, align, at);
        break;
      }
      case 10: readMemarg(r); r.skip(1); break;
      case 11: {
        const n = r.readU32Leb();
        for (let i = 0; i < n; i++) r.readU32Leb();
        r.readU32Leb();
        break;
      }
      case 12: r.skip(r.readU32Leb()); break;
      case 13:
      case 14: r.skip(16); break;
      case 15:
      case 16:
      case 17: r.skip(1); break;
    }
    if (pushedConst !== null) {
      constWindow.push(pushedConst);
      if (constWindow.length > 3) constWindow.shift();
    } else {
      constWindow.length = 0;
    }
  }
  return { partial: r.pos !== bodyEnd, stoppedAt: r.pos };
}

function readSections(r) {
  const magic = r.readU32Fixed();
  if (magic !== WASM_MAGIC) throw new Error("invalid WASM magic number");
  const version = r.readU32Fixed();
  if (version !== WASM_VERSION) throw new Error(`unsupported WASM version: ${version}`);
  const sections = [];
  while (r.pos < r.length) {
    const id = r.readByte();
    const offset = r.pos - 1;
    const size = r.readU32Leb();
    const contentOffset = r.pos;
    sections.push({ id, offset, size, contentOffset });
    r.pos = contentOffset + size;
  }
  return sections;
}

function readFuncType(r) {
  const tag = r.readByte();
  if (tag !== 0x60) throw new Error(`expected functype tag, got 0x${tag.toString(16)}`);
  const params = r.readVec(() => r.readValType());
  const results = r.readVec(() => r.readValType());
  return { params, results };
}

function readLimits(r) {
  const flags = r.readByte();
  const initial = r.readU32Leb();
  const maximum = flags & 0x01 ? r.readU32Leb() : undefined;
  return { initial, maximum };
}

function readGlobalType(r) {
  return { valueType: r.readValType(), mutable: r.readByte() === 0x01 };
}

function parseElementSection(r, section) {
  r.pos = section.contentOffset;
  return r.readVec(() => {
    const flags = r.readU32Leb();
    const active = (flags & 0x01) === 0;
    const explicitTable = (flags & 0x02) !== 0;
    const usesExpr = (flags & 0x04) !== 0;
    const declared = !active && explicitTable;
    let tableIndex = 0;
    let offset = null;
    if (active) {
      if (explicitTable) tableIndex = r.readU32Leb();
      const off = decodeConstExpr(r);
      offset = off.kind === "i32" ? off.value : null;
    }
    if ((flags & 0x03) !== 0) r.readByte();
    const funcIndices = [];
    const count = r.readU32Leb();
    for (let i = 0; i < count; i++) {
      if (usesExpr) {
        const item = decodeConstExpr(r);
        funcIndices.push(item.kind === "ref.func" ? item.index : -1);
      } else {
        funcIndices.push(r.readU32Leb());
      }
    }
    return { mode: active ? "active" : declared ? "declared" : "passive", tableIndex, offset, funcIndices };
  });
}

function parseDataSection(r, section) {
  r.pos = section.contentOffset;
  return r.readVec(() => {
    const flags = r.readU32Leb();
    const passive = flags === 1;
    let memoryIndex = 0;
    let offset = null;
    if (!passive) {
      if (flags === 2) memoryIndex = r.readU32Leb();
      const off = decodeConstExpr(r);
      offset = off.kind === "i32" ? off.value : null;
    }
    const byteLength = r.readU32Leb();
    const fileOffset = r.pos;
    r.skip(byteLength);
    return { mode: passive ? "passive" : "active", memoryIndex, offset, byteLength, fileOffset };
  });
}

function parseNameSection(r, section) {
  const names = new Map();
  r.pos = section.contentOffset;
  const customName = r.readName();
  if (customName !== "name") return names;
  const end = section.contentOffset + section.size;
  while (r.pos < end) {
    const subsectionId = r.readByte();
    const subsectionSize = r.readU32Leb();
    const subsectionEnd = r.pos + subsectionSize;
    if (subsectionId === 1) {
      const count = r.readU32Leb();
      for (let i = 0; i < count; i++) names.set(r.readU32Leb(), r.readName());
    }
    r.pos = subsectionEnd;
  }
  return names;
}

function analyzeWasm(name, binary) {
  const r = new BinaryReader(binary);
  const rawSections = readSections(r);
  let types = [], imports = [], typeIndices = [], tables = [], memories = [], globals = [];
  let exports = [], functions = [], elements = [], dataSegments = [], startFunction;
  const customSections = [];
  let functionNames = new Map();
  const sectionSizes = { custom: {} };

  for (const section of rawSections) {
    const key = Object.entries(SECTION).find(([, id]) => id === section.id)?.[0]?.toLowerCase();
    if (section.id === SECTION.CUSTOM) {
      const saved = r.pos;
      r.pos = section.contentOffset;
      const customName = r.readName();
      r.pos = saved;
      customSections.push({ name: customName, offset: section.offset, size: section.size });
      sectionSizes.custom[customName] = (sectionSizes.custom[customName] || 0) + section.size;
    } else if (key) {
      sectionSizes[key] = section.size;
    }
  }

  for (const section of rawSections) {
    if (section.id !== SECTION.CUSTOM) continue;
    const saved = r.pos;
    r.pos = section.contentOffset;
    const customName = r.readName();
    r.pos = saved;
    if (customName === "name") functionNames = parseNameSection(r, section);
  }

  for (const section of rawSections) {
    r.pos = section.contentOffset;
    switch (section.id) {
      case SECTION.TYPE: types = r.readVec(() => readFuncType(r)); break;
      case SECTION.IMPORT:
        imports = r.readVec(() => {
          const module = r.readName();
          const importName = r.readName();
          const kindByte = r.readByte();
          const entry = { module, name: importName, kind: EXTERNAL_KIND[kindByte] ?? "function" };
          if (kindByte === 0) {
            entry.typeIndex = r.readU32Leb();
            entry.type = types[entry.typeIndex];
          } else if (kindByte === 1) {
            entry.tableType = { elementType: r.readValType(), ...readLimits(r) };
          } else if (kindByte === 2) {
            entry.memoryType = readLimits(r);
          } else if (kindByte === 3) {
            entry.globalType = readGlobalType(r);
          }
          return entry;
        });
        break;
      case SECTION.FUNCTION: typeIndices = r.readVec(() => r.readU32Leb()); break;
      case SECTION.TABLE:
        tables = r.readVec(() => ({ elementType: r.readValType(), ...readLimits(r) }));
        break;
      case SECTION.MEMORY: memories = r.readVec(() => readLimits(r)); break;
      case SECTION.GLOBAL:
        globals = r.readVec(() => {
          const type = readGlobalType(r);
          const init = decodeConstExpr(r);
          return { index: globals.length, ...type, init };
        });
        break;
      case SECTION.EXPORT:
        exports = r.readVec(() => ({ name: r.readName(), kind: EXTERNAL_KIND[r.readByte()] ?? "function", index: r.readU32Leb() }));
        break;
      case SECTION.START: startFunction = r.readU32Leb(); break;
      case SECTION.ELEMENT: elements = parseElementSection(r, section); break;
      case SECTION.DATA: dataSegments = parseDataSection(r, section); break;
    }
  }

  const importedFuncCount = imports.filter((i) => i.kind === "function").length;
  const code = rawSections.find((s) => s.id === SECTION.CODE);
  if (code) {
    r.pos = code.contentOffset;
    const count = r.readU32Leb();
    for (let i = 0; i < count; i++) {
      const bodySize = r.readU32Leb();
      const bodyOffset = r.pos;
      const funcIndex = importedFuncCount + i;
      const typeIndex = typeIndices[i] ?? -1;
      const localDecls = [];
      const localDeclCount = r.readU32Leb();
      for (let j = 0; j < localDeclCount; j++) {
        const localCount = r.readU32Leb();
        const localType = r.readValType();
        localDecls.push({ count: localCount, type: localType });
      }
      functions.push({
        index: funcIndex,
        name: functionNames.get(funcIndex),
        typeIndex,
        type: types[typeIndex] ?? { params: [], results: [] },
        bodyOffset,
        bodySize,
        localDecls,
      });
      r.pos = bodyOffset + bodySize;
    }
  }

  for (const exp of exports) {
    if (exp.kind !== "function") continue;
    exp.type = exp.index < importedFuncCount
      ? imports[exp.index]?.type
      : functions[exp.index - importedFuncCount]?.type;
  }
  for (const imp of imports) {
    if (imp.kind === "memory" && imp.memoryType) memories.push(imp.memoryType);
    if (imp.kind === "table" && imp.tableType) tables.push(imp.tableType);
  }

  return {
    name,
    types,
    imports,
    exports,
    functions,
    memories,
    tables,
    globals,
    startFunction,
    customSections,
    sectionSizes,
    totalSize: binary.length,
    elements,
    dataSegments,
  };
}

const ASCII_DECODER = new TextDecoder("latin1");
function isPrintable(b) { return b >= 0x20 && b < 0x7f; }

class WasmIndex {
  constructor(binary, analysis) {
    this.bytes = binary instanceof Buffer ? new Uint8Array(binary.buffer, binary.byteOffset, binary.byteLength) : binary;
    this.analysis = analysis;
    this.reader = new BinaryReader(this.bytes);
    this.funcTypeIndex = new Map();
    this.funcName = new Map();
    let funcIdx = 0;
    for (const imp of analysis.imports) {
      if (imp.kind === "function") this.funcTypeIndex.set(funcIdx++, imp.typeIndex ?? -1);
    }
    for (const fn of analysis.functions) {
      this.funcTypeIndex.set(fn.index, fn.typeIndex);
      if (fn.name) this.funcName.set(fn.index, fn.name);
    }
  }
  slotMap() {
    if (this._slotMap) return this._slotMap;
    const map = new Map();
    const tableFuncs = new Map();
    for (const seg of this.analysis.elements || []) {
      if (seg.mode !== "active" || seg.offset == null) continue;
      if (!map.has(seg.tableIndex)) map.set(seg.tableIndex, new Map());
      if (!tableFuncs.has(seg.tableIndex)) tableFuncs.set(seg.tableIndex, new Set());
      const table = map.get(seg.tableIndex);
      const funcs = tableFuncs.get(seg.tableIndex);
      for (let k = 0; k < seg.funcIndices.length; k++) {
        const fn = seg.funcIndices[k];
        if (fn < 0) continue;
        table.set(seg.offset + k, fn);
        funcs.add(fn);
      }
    }
    this._slotMap = map;
    this._tableFuncs = tableFuncs;
    return map;
  }
  funcAtSlot(slot, tableIndex = 0) {
    return this.slotMap().get(tableIndex)?.get(slot) ?? -1;
  }
  tableFuncs() {
    if (!this._tableFuncs) this.slotMap();
    return this._tableFuncs;
  }
  resolveIndirect(typeIndex, tableIndex = 0) {
    const inTable = this.tableFuncs().get(tableIndex);
    if (!inTable) return [];
    return [...inTable].filter((fn) => this.funcTypeIndex.get(fn) === typeIndex);
  }
  passiveBases() {
    if (this._passiveBases) return this._passiveBases;
    const out = new Map();
    for (const fn of this.analysis.functions) {
      walkFunctionBody(this.reader, fn.bodyOffset, fn.bodySize, {
        onMemoryInit: (dataIndex, destAddr) => {
          if (destAddr != null && !out.has(dataIndex)) out.set(dataIndex, destAddr);
        },
      });
    }
    this._passiveBases = out;
    return out;
  }
  segments() {
    if (this._segments) return this._segments;
    const passiveBases = (this.analysis.dataSegments || []).some((s) => s.mode === "passive") ? this.passiveBases() : new Map();
    const segs = [];
    for (let i = 0; i < (this.analysis.dataSegments || []).length; i++) {
      const seg = this.analysis.dataSegments[i];
      const base = seg.mode === "active" ? seg.offset : passiveBases.get(i) ?? null;
      if (base == null) continue;
      segs.push({ index: i, base, bytes: this.bytes.subarray(seg.fileOffset, seg.fileOffset + seg.byteLength), ...seg });
    }
    segs.sort((a, b) => a.base - b.base);
    this._segments = segs;
    return segs;
  }
  readMemory(addr, len) {
    for (const seg of this.segments()) {
      if (addr >= seg.base && addr + len <= seg.base + seg.bytes.length) {
        const off = addr - seg.base;
        return seg.bytes.subarray(off, off + len);
      }
    }
    return null;
  }
  readU32(addr) {
    const b = this.readMemory(addr, 4);
    if (!b) return null;
    return (b[0] | (b[1] << 8) | (b[2] << 16) | (b[3] << 24)) >>> 0;
  }
  stringEntries() {
    if (this._stringEntries) return this._stringEntries;
    const entries = [];
    for (const seg of this.segments()) {
      let i = 0;
      while (i < seg.bytes.length) {
        const start = i;
        while (i < seg.bytes.length && isPrintable(seg.bytes[i])) i++;
        if (i - start >= 4) entries.push({ address: seg.base + start, value: ASCII_DECODER.decode(seg.bytes.subarray(start, i)) });
        while (i < seg.bytes.length && !isPrintable(seg.bytes[i])) i++;
      }
    }
    entries.sort((a, b) => a.address - b.address);
    this._stringEntries = entries;
    return entries;
  }
  stringContaining(addr) {
    const entries = this.stringEntries();
    let lo = 0, hi = entries.length - 1, ans = -1;
    while (lo <= hi) {
      const mid = (lo + hi) >>> 1;
      if (entries[mid].address <= addr) { ans = mid; lo = mid + 1; }
      else hi = mid - 1;
    }
    if (ans < 0) return null;
    const e = entries[ans];
    return addr >= e.address && addr < e.address + e.value.length ? e.value : null;
  }
  codeScan() {
    if (this._callGraph && this._stringRefs) return;
    const calls = new Map(), callers = new Map(), indirect = new Map();
    const byFunction = new Map(), byString = new Map();
    const addEdge = (from, to) => {
      if (!calls.has(from)) calls.set(from, new Set());
      calls.get(from).add(to);
      if (!callers.has(to)) callers.set(to, new Set());
      callers.get(to).add(from);
    };
    for (const fn of this.analysis.functions) {
      const seenStrings = new Set();
      walkFunctionBody(this.reader, fn.bodyOffset, fn.bodySize, {
        onCall: (callee) => addEdge(fn.index, callee),
        onRefFunc: (callee) => addEdge(fn.index, callee),
        onCallIndirect: (typeIndex, tableIndex, at) => {
          if (!indirect.has(fn.index)) indirect.set(fn.index, []);
          indirect.get(fn.index).push({ typeIndex, tableIndex, at });
        },
        onI32Const: (value) => {
          const str = this.stringContaining(value);
          if (str == null || seenStrings.has(str)) return;
          seenStrings.add(str);
          if (!byFunction.has(fn.index)) byFunction.set(fn.index, []);
          byFunction.get(fn.index).push(str);
          if (!byString.has(str)) byString.set(str, []);
          byString.get(str).push(fn.index);
        },
      });
    }
    this._callGraph = { calls, callers, indirect };
    this._stringRefs = { byFunction, byString };
  }
  callGraph() { this.codeScan(); return this._callGraph; }
  stringRefs() { this.codeScan(); return this._stringRefs; }
}

